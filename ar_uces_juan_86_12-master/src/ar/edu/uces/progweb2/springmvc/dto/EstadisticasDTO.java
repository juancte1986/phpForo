package ar.edu.uces.progweb2.springmvc.dto;

public class EstadisticasDTO {
	
	private String numTemas;
	private String numRespuestas;
	private String numUsuarios;
	private String numJava;
	private String numPhp;
	private String numNet;
	private String numC;
	
	public String getNumTemas() {
		return numTemas;
	}
	public void setNumTemas(String numTemas) {
		this.numTemas = numTemas;
	}
	public String getNumRespuestas() {
		return numRespuestas;
	}
	public void setNumRespuestas(String numRespuestas) {
		this.numRespuestas = numRespuestas;
	}
	public String getNumUsuarios() {
		return numUsuarios;
	}
	public void setNumUsuarios(String numUsuarios) {
		this.numUsuarios = numUsuarios;
	}
	public String getNumJava() {
		return numJava;
	}
	public void setNumJava(String numJava) {
		this.numJava = numJava;
	}
	public String getNumPhp() {
		return numPhp;
	}
	public void setNumPhp(String numPhp) {
		this.numPhp = numPhp;
	}
	public String getNumNet() {
		return numNet;
	}
	public void setNumNet(String numNet) {
		this.numNet = numNet;
	}
	public String getNumC() {
		return numC;
	}
	public void setNumC(String numC) {
		this.numC = numC;
	}
	
}
